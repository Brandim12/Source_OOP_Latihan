package Struktur_control;

public class PerulanganFor {

    public static void main (String []args) {
        //for ascending
        System.out.println("For Ascending");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        //for descending
        System.out.println("For Descending");
        for (int i = 5; i >= 1; i--) {
            System.out.println(i);
        }
    }
}
